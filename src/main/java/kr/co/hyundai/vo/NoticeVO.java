package kr.co.hyundai.vo;

public class NoticeVO {
	private int noticeNum;
	private String noticeSubject;
	private String noticeWrite;
	private String noticeContent;
	private String noticeReg_date;
	
	
	public int getNoticeNum() {
		return noticeNum;
	}
	public void setNoticeNum(int noticeNum) {
		this.noticeNum = noticeNum;
	}
	public String getNoticeSubject() {
		return noticeSubject;
	}
	public void setNoticeSubject(String noticeSubject) {
		this.noticeSubject = noticeSubject;
	}
	public String getNoticeWrite() {
		return noticeWrite;
	}
	public void setNoticeWrite(String noticeWrite) {
		this.noticeWrite = noticeWrite;
	}
	public String getNoticeContent() {
		return noticeContent;
	}
	public void setNoticeContent(String noticeContent) {
		this.noticeContent = noticeContent;
	}
	public String getNoticeReg_date() {
		return noticeReg_date;
	}
	public void setNoticeReg_date(String noticeReg_date) {
		this.noticeReg_date = noticeReg_date;
	}
	
	
}
