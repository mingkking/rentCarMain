package kr.co.hyundai.vo;

public class CarViewVO {
	private int carRNo;
	private String carName;
	private int price;
	private String img;
	private int cnt;
	private int day;
	private String dday;
	private int insurance;
	private int wifi;
	private int babySeat;
	private int navi;
	
	public int getCarRNo() {
		return carRNo;
	}
	public void setCarRNo(int carRNo) {
		this.carRNo = carRNo;
	}
	public String getCarName() {
		return carName;
	}
	public void setCarName(String carName) {
		this.carName = carName;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public String getDday() {
		return dday;
	}
	public void setDday(String dday) {
		this.dday = dday;
	}
	public int getInsurance() {
		return insurance;
	}
	public void setInsurance(int insurance) {
		this.insurance = insurance;
	}
	public int getWifi() {
		return wifi;
	}
	public void setWifi(int wifi) {
		this.wifi = wifi;
	}
	public int getBabySeat() {
		return babySeat;
	}
	public void setBabySeat(int babySeat) {
		this.babySeat = babySeat;
	}
	public int getNavi() {
		return navi;
	}
	public void setNavi(int navi) {
		this.navi = navi;
	}
	
}
